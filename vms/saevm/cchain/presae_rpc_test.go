// TestPreSAETraceBlockRPCs checks parity with the following methods:
//   - debug_traceBlockByNumber
//   - debug_traceBlockByHash
//
// Tracer configs shared by the debug_trace* tests. The callTracer reports
// each transaction's tree of EVM calls; the prestateTracer reports the state
// each transaction read, or the state changes it made in diff mode.
var (
	callTracer         = tracers.TraceConfig{Tracer: utils.PointerTo("callTracer")}
	prestateTracer     = tracers.TraceConfig{Tracer: utils.PointerTo("prestateTracer")}
	prestateDiffTracer = tracers.TraceConfig{
		Tracer:       utils.PointerTo("prestateTracer"),
		TracerConfig: json.RawMessage(`{"diffMode": true}`),
	}
)

func TestPreSAETraceBlockRPCs(t *testing.T) {
	ctx, sut, fx := newPreSAESUT(t)

	forEachBlock(t, fx, func(t *testing.T, fxBlock upgradechaintest.Block) {
		eth := fxBlock.EthBlock(t)

		// Tracing is expected to fail on two kinds of block: genesis, which has
		// no transactions to execute, and blocks carrying a functional
		// nativeAssetCall, whose nested EVM call breaks the tracer (see
		// [upgradechaintest.NativeAssetCallBlocks]).
		wantErr := ""
		switch {
		case fxBlock.Number == 0:
			wantErr = upgradechaintest.GenesisTraceError
		case fxBlock.HasNativeAssetCall():
			wantErr = upgradechaintest.NativeAssetCallTraceError
		}

		tests := []struct {
			method string
			arg    any
		}{
			{"debug_traceBlockByNumber", hexutil.Uint64(fxBlock.Number)},
			{"debug_traceBlockByHash", fxBlock.Hash},
		}
		for _, tt := range tests {
			t.Run(tt.method, func(t *testing.T) {
				var traces []tracers.TxTraceResult
				err := sut.ethclient.Client().CallContext(ctx, &traces, tt.method, tt.arg, callTracer)
				if wantErr != "" {
					require.EqualError(t, err, wantErr, "%s(%v)", tt.method, tt.arg)
					return
				}
				require.NoError(t, err, "%s(%v)", tt.method, tt.arg)
				require.Len(t, traces, len(eth.Transactions()), "traces per EVM transaction")
				for i, trace := range traces {
					assert.Empty(t, trace.Error, "trace %d error", i)
					assert.NotEmpty(t, trace.Result, "trace %d result", i)
				}
			})
		}

		if wantErr == "" {
			assertTraceReplaysIntraBlockState(ctx, t, sut, fxBlock, eth)
		}
	})
}

// assertTraceReplaysIntraBlockState asserts that block tracing executes
// transaction i on the state left by transactions 0..i-1, not on the block's
// parent state.
func assertTraceReplaysIntraBlockState(ctx context.Context, t *testing.T, sut *SUT, fxBlock upgradechaintest.Block, eth *types.Block) {
	t.Helper()

	var prestates []struct {
		Result map[common.Address]native.Account `json:"result"`
	}
	require.NoError(t,
		sut.ethclient.Client().CallContext(ctx, &prestates, "debug_traceBlockByNumber", hexutil.Uint64(fxBlock.Number), prestateTracer),
		"debug_traceBlockByNumber(%d, prestateTracer)", fxBlock.Number)
	require.Len(t, prestates, len(eth.Transactions()), "prestates per EVM transaction")

	assertCoinbaseReplayFees(ctx, t, sut, fxBlock, eth, func(i int) map[common.Address]native.Account {
		return prestates[i].Result
	})
}

// TestPreSAETraceTransactionRPCs checks parity with debug_traceTransaction.
func TestPreSAETraceTransactionRPCs(t *testing.T) {
	ctx, sut, fx := newPreSAESUT(t)

	forEachBlock(t, fx, func(t *testing.T, fxBlock upgradechaintest.Block) {
		eth := fxBlock.EthBlock(t)
		receipts, err := sut.ethclient.BlockReceipts(ctx, rpc.BlockNumberOrHashWithHash(fxBlock.Hash, false))
		require.NoError(t, err, "BlockReceipts(%s)", fxBlock.Hash)
		require.Len(t, receipts, len(eth.Transactions()), "receipts per EVM transaction")

		t.Run("debug_traceTransaction", func(t *testing.T) {
			for i, tx := range eth.Transactions() {
				var trace native.CallFrame
				err := sut.ethclient.Client().CallContext(ctx, &trace, "debug_traceTransaction", tx.Hash(), callTracer)
				if fxBlock.HasNativeAssetCall() {
					require.EqualError(t, err, upgradechaintest.NativeAssetCallTraceError, "debug_traceTransaction(%s) matches the historical C-Chain", tx.Hash())
					continue
				}
				require.NoError(t, err, "debug_traceTransaction(%s)", tx.Hash())
				assert.Equal(t, vmtest.TestEthAddrs[0], trace.From, "traced sender of %s", tx.Hash())

				// Pins historical C-Chain gas accounting; notably the SSTORE refund counter
				// is disabled from Apricot Phase 1, which EIP-3529 replay
				// semantics would violate for the storage-clearing transaction.
				assert.Equal(t, receipts[i].GasUsed, trace.GasUsed, "traced gasUsed of %s matches the stored receipt", tx.Hash())
			}
		})

		if fxBlock.Number == 0 || fxBlock.HasNativeAssetCall() {
			// Genesis has no transactions and nativeAssetCall blocks are not
			// traceable (asserted above).
			return
		}

		// Transaction i's reported pre-state MUST include the coinbase fees of
		// transactions 0..i-1, priced with the block's historical base fee.
		t.Run("debug_traceTransaction_prestate", func(t *testing.T) {
			assertCoinbaseReplayFees(ctx, t, sut, fxBlock, eth, func(i int) map[common.Address]native.Account {
				tx := eth.Transactions()[i]
				var prestate map[common.Address]native.Account
				require.NoError(t,
					sut.ethclient.Client().CallContext(ctx, &prestate, "debug_traceTransaction", tx.Hash(), prestateTracer),
					"debug_traceTransaction(%s, prestateTracer)", tx.Hash())
				return prestate
			})
		})

		// Transaction i's reported post-state MUST also include its own fee,
		// notably the base-fee portion credited by the
		// AfterExecutingTransaction hook.
		t.Run("debug_traceTransaction_poststate", func(t *testing.T) {
			coinbase := eth.Coinbase()
			for i, tx := range eth.Transactions() {
				var diff struct {
					Pre  map[common.Address]native.Account `json:"pre"`
					Post map[common.Address]native.Account `json:"post"`
				}
				require.NoError(t,
					sut.ethclient.Client().CallContext(ctx, &diff, "debug_traceTransaction", tx.Hash(),
						prestateDiffTracer),
					"debug_traceTransaction(%s, prestateTracer diffMode)", tx.Hash())

				post, ok := diff.Post[coinbase]
				require.Truef(t, ok, "coinbase %s in post-state diff of tx %d", coinbase, i)
				credited := new(big.Int).Set(post.Balance)
				if pre, ok := diff.Pre[coinbase]; ok { // absent when the coinbase did not yet exist
					credited.Sub(credited, pre.Balance)
				}

				fee := new(big.Int).SetUint64(receipts[i].GasUsed)
				fee.Mul(fee, receipts[i].EffectiveGasPrice)
				assert.Zerof(t, fee.Cmp(credited),
					"coinbase credit of tx %d: want %s, got %s", i, fee, credited)
			}
		})
	})
}

// assertCoinbaseReplayFees asserts that prestate(i), a tracing endpoint's
// pre-state for transaction i, shows the coinbase at the parent block's
// balance plus the full fees (gasUsed * effectiveGasPrice) of transactions
// 0..i-1, pinning both the replay's intra-block ordering and its base fee.
func assertCoinbaseReplayFees(ctx context.Context, t *testing.T, sut *SUT, fxBlock upgradechaintest.Block, eth *types.Block, prestate func(i int) map[common.Address]native.Account) {
	t.Helper()

	receipts, err := sut.ethclient.BlockReceipts(ctx, rpc.BlockNumberOrHashWithHash(fxBlock.Hash, false))
	require.NoError(t, err, "BlockReceipts(%s)", fxBlock.Hash)
	require.Len(t, receipts, len(eth.Transactions()), "receipts per EVM transaction")

	coinbase := eth.Coinbase()
	want, err := sut.ethclient.BalanceAt(ctx, coinbase, new(big.Int).SetUint64(fxBlock.Number-1))
	require.NoError(t, err, "BalanceAt(%s, %d)", coinbase, fxBlock.Number-1)
	for i, r := range receipts {
		pre, ok := prestate(i)[coinbase]
		require.True(t, ok, "coinbase %s in prestate of tx %d", coinbase, i)
		assert.Zerof(t, want.Cmp(pre.Balance),
			"coinbase balance in prestate of tx %d: want %s, got %s", i, want, pre.Balance)

		fee := new(big.Int).SetUint64(r.GasUsed)
		want.Add(want, fee.Mul(fee, r.EffectiveGasPrice))
	}
}

