// newPreSAESUT boots the cchain VM on the [upgradechaintest] fixture's database,
// modelling a node whose historical C-Chain data crosses every network
// upgrade and which has just switched to this VM. All blocks are below the
// last-settled height and absent from VM memory, so every query takes the
// on-disk restore path for synchronous (pre-SAE) blocks.
//
// Each TestPreSAE* test boots its own SUT so that a failure's logs cover
// only the RPCs under test and every RPC family hits the VM's caches cold.
func newPreSAESUT(t *testing.T) (context.Context, *SUT, *upgradechaintest.Fixture) {
	t.Helper()

	fx := upgradechaintest.Load(t)

	// The fixture was dumped from the database handle the historical C-Chain
	// VM received, which corresponds to [chainDBPrefix] of the SUT's base
	// database.
	db := memdb.New()
	fx.SeedDatabase(t, prefixdb.New(chainDBPrefix, db))

	tipTime := fx.Tip().Timestamp()
	timeOpt, _ := withVMTime(tipTime.Add(10 * time.Second))

	// Schedule the SAE-era Helicon upgrade just past the synchronous tip so
	// that the historical chain remains valid under it.
	upgrades := fx.Upgrades
	upgrades.HeliconTime = tipTime.Add(time.Second)

	ctx, sut := newSUT(t,
		withDB(db),
		withGenesis(fx.CoreGenesis(t)),
		withUpgrades(upgrades),
		// The fixture was generated by a non-pruning node, which marked the
		// database to refuse later pruning runs.
		withArchival(),
		timeOpt,
	)
	return ctx, sut, fx
}

// forEachBlock runs test as a subtest against every fixture block, logging
// the block's note.
func forEachBlock(t *testing.T, fx *upgradechaintest.Fixture, test func(t *testing.T, fxBlock upgradechaintest.Block)) {
	for _, fxBlock := range fx.Blocks {
		t.Run(fmt.Sprintf("block_%02d_%s", fxBlock.Number, fxBlock.Fork), func(t *testing.T) {
			t.Logf("%s", fxBlock.Note)
			test(t, fxBlock)
		})
	}
}

