// TestPreSAEFeeHistoryRPC checks parity with eth_feeHistory.
func TestPreSAEFeeHistoryRPC(t *testing.T) {
	ctx, sut, fx := newPreSAESUT(t)

	// A single percentile keeps the reward matrix one column wide; the
	// percentile arithmetic itself is pinned by the gasprice package's tests.
	percentiles := []float64{50}
	numBlocks := uint64(len(fx.Blocks))
	hist, err := sut.ethclient.FeeHistory(ctx, numBlocks, nil, percentiles)
	require.NoError(t, err, "FeeHistory(%d, latest, %v)", numBlocks, percentiles)

	require.Zero(t, hist.OldestBlock.Uint64(), "history covers the whole chain, starting at genesis")
	require.Len(t, hist.BaseFee, len(fx.Blocks)+1, "one base fee per block plus the next block's estimate")
	require.Len(t, hist.GasUsedRatio, len(fx.Blocks), "one gas-used ratio per block")
	require.Len(t, hist.Reward, len(fx.Blocks), "one reward row per block")

	for i, fxBlock := range fx.Blocks {
		eth := fxBlock.EthBlock(t)

		// Blocks before ApricotPhase3's London rules carry no base fee, which
		// eth_feeHistory reports as zero.
		wantBaseFee := eth.BaseFee()
		if wantBaseFee == nil {
			wantBaseFee = new(big.Int)
		}
		assert.Zerof(t, wantBaseFee.Cmp(hist.BaseFee[i]), "base fee of block %d: want %s, got %s",
			fxBlock.Number, wantBaseFee, hist.BaseFee[i])
		assert.Equal(t, float64(eth.GasUsed())/float64(eth.GasLimit()), hist.GasUsedRatio[i], "gas-used ratio of block %d", fxBlock.Number)

		require.Len(t, hist.Reward[i], len(percentiles), "rewards per requested percentile of block %d", fxBlock.Number)
		gotReward := hist.Reward[i][0]
		if txs := eth.Transactions(); len(txs) == 0 { // genesis
			assert.Zero(t, gotReward.Sign(), "reward of empty block %d", fxBlock.Number)
		} else {
			// The reward MUST be a tip paid by one of the block's transactions
			// (for single-transaction blocks, exactly its tip).
			paid := slices.ContainsFunc(txs, func(tx *types.Transaction) bool {
				return tx.EffectiveGasTipValue(wantBaseFee).Cmp(gotReward) == 0
			})
			assert.Truef(t, paid, "reward %s of block %d is a tip paid by one of its transactions", gotReward, fxBlock.Number)
		}
	}

	// The trailing entry estimates the next block's base fee, which for a
	// fully synchronous chain is the tip's own base fee.
	wantNext := fx.Tip().EthBlock(t).BaseFee()
	gotNext := hist.BaseFee[len(fx.Blocks)]
	assert.Zerof(t, wantNext.Cmp(gotNext), "next-block base fee estimate: want %s, got %s", wantNext, gotNext)
}

// TestPreSAETraceChainRPC checks parity with debug_traceChain.
func TestPreSAETraceChainRPC(t *testing.T) {
	ctx, sut, fx := newPreSAESUT(t)
	ctx, cancel := context.WithTimeout(ctx, time.Minute)
	defer cancel()

	tip := fx.Tip()
	results := make(chan tracers.BlockTraceResult)
	sub, err := sut.ethclient.Client().Subscribe(ctx, "debug", results, "traceChain",
		hexutil.Uint64(0), hexutil.Uint64(tip.Number), callTracer)
	require.NoError(t, err, "Subscribe(debug_traceChain, 0, %d)", tip.Number)
	defer sub.Unsubscribe()

	// The subscription streams one result per non-empty block in (start, end]
	// in ascending height order; every fixture block after genesis carries
	// transactions.
	for _, fxBlock := range fx.Blocks[1:] {
		var got tracers.BlockTraceResult
		select {
		case got = <-results:
		case err := <-sub.Err():
			t.Fatalf("subscription closed waiting for block %d: %v", fxBlock.Number, err)
		case <-ctx.Done():
			t.Fatalf("timed out waiting for block %d: %v", fxBlock.Number, ctx.Err())
		}

		assert.Equal(t, fxBlock.Number, uint64(got.Block), "streamed block number")
		assert.Equal(t, fxBlock.Hash, got.Hash, "streamed hash is the canonical hash of block %d", fxBlock.Number)

		txs := fxBlock.EthBlock(t).Transactions()
		require.Len(t, got.Traces, len(txs), "traces per EVM transaction of block %d", fxBlock.Number)
		for i, trace := range got.Traces {
			assert.Equal(t, txs[i].Hash(), trace.TxHash, "trace %d tx hash of block %d", i, fxBlock.Number)
			// Unlike the debug_traceBlock* endpoints, which fail outright on
			// blocks carrying a functional nativeAssetCall, debug_traceChain
			// reports the failure per transaction and keeps streaming.
			if fxBlock.HasNativeAssetCall() {
				assert.Equal(t, upgradechaintest.NativeAssetCallTraceError, trace.Error, "trace %d error of block %d", i, fxBlock.Number)
				continue
			}
			assert.Empty(t, trace.Error, "trace %d error of block %d", i, fxBlock.Number)
			assert.NotEmpty(t, trace.Result, "trace %d result of block %d", i, fxBlock.Number)
		}
	}
}
