// TestPreSAEBlockLookupRPCs checks parity with the following methods:
//   - eth_getBlockByNumber
//   - eth_getBlockByHash
func TestPreSAEBlockLookupRPCs(t *testing.T) {
	ctx, sut, fx := newPreSAESUT(t)

	forEachBlock(t, fx, func(t *testing.T, fxBlock upgradechaintest.Block) {
		eth := fxBlock.EthBlock(t)
		opts := cmp.Options{
			cmputils.Blocks(),
			cmputils.Headers(),
			cmpopts.EquateEmpty(),
		}

		byNumber, err := sut.ethclient.BlockByNumber(ctx, new(big.Int).SetUint64(fxBlock.Number))
		require.NoError(t, err, "BlockByNumber(%d)", fxBlock.Number)
		if diff := cmp.Diff(eth, byNumber, opts); diff != "" {
			t.Errorf("BlockByNumber(%d) diff (-want +got):\n%s", fxBlock.Number, diff)
		}

		byHash, err := sut.ethclient.BlockByHash(ctx, fxBlock.Hash)
		require.NoError(t, err, "BlockByHash(%s)", fxBlock.Hash)
		if diff := cmp.Diff(eth, byHash, opts); diff != "" {
			t.Errorf("BlockByHash(%s) diff (-want +got):\n%s", fxBlock.Hash, diff)
		}
	})
}

// TestPreSAEStateRPCs checks parity with the following methods:
//   - eth_getBalance
//   - eth_getTransactionCount
//   - eth_getCode
//   - eth_getStorageAt
//   - eth_call
//   - eth_callDetailed
//   - eth_getProof
func TestPreSAEStateRPCs(t *testing.T) {
	ctx, sut, fx := newPreSAESUT(t)

	counter := fx.Counter
	forEachBlock(t, fx, func(t *testing.T, fxBlock upgradechaintest.Block) {
		blockNum := new(big.Int).SetUint64(fxBlock.Number)
		t.Run("balances_and_nonces", func(t *testing.T) {
			for addr, want := range fxBlock.State {
				gotBalance, err := sut.ethclient.BalanceAt(ctx, addr, blockNum)
				require.NoError(t, err, "BalanceAt(%s, %d)", addr, fxBlock.Number)
				assert.Zerof(t, want.Balance.ToInt().Cmp(gotBalance), "BalanceAt(%s, %d): want %s, got %s",
					addr, fxBlock.Number, want.Balance.ToInt(), gotBalance)

				gotNonce, err := sut.ethclient.NonceAt(ctx, addr, blockNum)
				require.NoError(t, err, "NonceAt(%s, %d)", addr, fxBlock.Number)
				assert.Equal(t, want.Nonce, gotNonce, "NonceAt(%s, %d)", addr, fxBlock.Number)
			}
		})

		// The fixture's counter contract (deployed in block 1) increments storage
		// slot 0 when called with empty call data and returns the slot otherwise.
		// [upgradechaintest.Block.Counter] records its expected value at every height.
		t.Run("counter_contract", func(t *testing.T) {
			code, err := sut.ethclient.CodeAt(ctx, counter, blockNum)
			require.NoError(t, err, "CodeAt(counter, %d)", fxBlock.Number)
			if fxBlock.Number == 0 {
				assert.Empty(t, code, "counter contract code before its block-1 deployment")
			} else {
				assert.NotEmpty(t, code, "counter contract code")
			}

			slot0, err := sut.ethclient.StorageAt(ctx, counter, common.Hash{}, blockNum)
			require.NoError(t, err, "StorageAt(counter, 0, %d)", fxBlock.Number)
			assert.Equal(t, fxBlock.Counter, new(big.Int).SetBytes(slot0).Uint64(), "counter storage slot 0")

			got, err := sut.ethclient.CallContract(ctx, ethereum.CallMsg{
				To:   &counter,
				Data: []byte{1}, // any non-empty call data returns slot 0
			}, blockNum)
			require.NoError(t, err, "CallContract(counter, %d)", fxBlock.Number)
			assert.Equal(t, fxBlock.Counter, new(big.Int).SetBytes(got).Uint64(), "eth_call returned counter value")
		})

		// eth_callDetailed's semantics are pinned by the sae package's
		// TestCallDetailed; this only checks that it serves historical state.
		t.Run("eth_callDetailed", func(t *testing.T) {
			var got saerpc.DetailedExecutionResult
			err := sut.ethclient.Client().CallContext(ctx, &got, "eth_callDetailed",
				ethapi.TransactionArgs{
					To:   &counter,
					Data: utils.PointerTo(hexutil.Bytes{1}), // any non-empty call data returns slot 0
				},
				rpc.BlockNumber(fxBlock.Number), //#nosec G115 -- Known non-negative
			)
			require.NoError(t, err, "eth_callDetailed(counter, %d)", fxBlock.Number)
			assert.Empty(t, got.Err, "execution error")
			assert.Zero(t, got.ErrCode, "execution error code")
			assert.NotZero(t, got.UsedGas, "gas used")
			assert.Equal(t, fxBlock.Counter, new(big.Int).SetBytes(got.ReturnData).Uint64(), "eth_callDetailed returned counter value")
		})

		t.Run("eth_getProof", func(t *testing.T) {
			proof, err := gethclient.New(sut.ethclient.Client()).GetProof(ctx, counter, nil, blockNum)
			require.NoError(t, err, "GetProof(counter, %d)", fxBlock.Number)

			want := fxBlock.State[counter]
			assert.Zero(t, want.Balance.ToInt().Cmp(proof.Balance), "proof balance")
			assert.Equal(t, want.Nonce, proof.Nonce, "proof nonce")

			// The proof MUST verify against the block's state root.
			saetest.VerifyProof(t, fxBlock.EthBlock(t).Root(), proof)
		})
	})
}

