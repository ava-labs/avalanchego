// A fixture is a complete, deterministic C-Chain history crossing every
// scheduled network upgrade, for tests that need realistic synchronous
// blocks and state; e.g. tests of the migration to this VM. It was
// generated by the historical (synchronous) C-Chain VM, activating each
// upgrade at a distinct timestamp and accepting at least one block per
// upgrade exercising an observable change of that upgrade.
type fixture struct {
	// Genesis is the JSON-encoded core.Genesis passed to the VM's
	// Initialize method.
	Genesis json.RawMessage `json:"genesis"`
	// Upgrades is the [upgrade.Config] the chain was generated with.
	// Upgrades after the tip's (Helicon onwards) are unscheduled.
	Upgrades upgrade.Config `json:"upgrades"`
	// ANTAssetID is the Avalanche-native-token asset ID imported in block 6
	// and moved by the fixture's nativeAssetCall transactions.
	ANTAssetID ids.ID `json:"antAssetID"`
	// Counter is the address of the fixture's counter contract, deployed in
	// block 1. A transaction (or eth_call) with empty call data increments
	// its storage slot 0; non-empty call data returns the slot's value as a
	// 32-byte word.
	Counter common.Address `json:"counter"`
	// Blocks holds the genesis block followed by every accepted block, in
	// height order, starting at height 0.
	Blocks []fixtureBlock `json:"blocks"`
	// Database holds every key-value pair of the VM's database after all
	// Blocks were accepted, keyed by the hex-encoded key. Written verbatim
	// to an empty database, they recreate the state a migrating node would
	// hand to this VM.
	Database map[string]hexutil.Bytes `json:"database"`
}

//go:embed testdata/upgradechain_fixture.json
var upgradechainFixtureJSON []byte

// loadFixture returns the frozen fixture.
func loadFixture(tb testing.TB) *fixture {
	tb.Helper()

	fx := new(fixture)
	require.NoError(tb, json.Unmarshal(upgradechainFixtureJSON, fx), "unmarshalling committed fixture")
	return fx
}

// tip returns the fixture's highest block.
func (f *fixture) tip() fixtureBlock {
	return f.Blocks[len(f.Blocks)-1]
}

// coreGenesis decodes [fixture.Genesis].
func (f *fixture) coreGenesis(tb testing.TB) core.Genesis {
	tb.Helper()

	var genesis core.Genesis
	require.NoError(tb, json.Unmarshal(f.Genesis, &genesis), "unmarshalling fixture genesis")
	return genesis
}

// seedDatabase writes every [fixture.Database] key-value pair to db,
// recreating the database a migrating node would hand to this VM.
func (f *fixture) seedDatabase(tb testing.TB, db database.KeyValueWriter) {
	tb.Helper()

	for keyHex, value := range f.Database {
		key, err := hexutil.Decode(keyHex)
		require.NoError(tb, err, "decoding fixture database key %s", keyHex)
		require.NoError(tb, db.Put(key, value), "seeding fixture database key %s", keyHex)
	}
}

// A fixtureBlock is a single accepted block of the fixture chain.
type fixtureBlock struct {
	// Fork names the network upgrade whose rules the block was built under.
	Fork string `json:"fork"`
	// Note describes what the block exercises.
	Note   string      `json:"note"`
	Number uint64      `json:"number"`
	Hash   common.Hash `json:"hash"`
	// Time is the block's timestamp in Unix seconds.
	Time uint64 `json:"time"`
	// RLP is the canonical encoding of the full block. Decoding it requires
	// the C-Chain's libevm extras to be registered; see customtypes.Register.
	RLP hexutil.Bytes `json:"rlp"`
	// State holds the watched accounts' post-execution state as the
	// historical C-Chain itself computed it, for asserting historical RPC
	// results against.
	State map[common.Address]accountState `json:"state"`
	// Counter is the value of the counter contract's storage slot 0 after
	// this block executed.
	Counter uint64 `json:"counterValue"`
	// IntermediateRoots holds the state root after each transaction, as
	// returned by debug_intermediateRoots. Nil for genesis, which is not
	// traceable.
	IntermediateRoots []common.Hash `json:"intermediateRoots,omitempty"`
}

// timestamp returns [fixtureBlock.Time] as a [time.Time].
func (b fixtureBlock) timestamp() time.Time {
	return time.Unix(int64(b.Time), 0) //#nosec G115 -- fixture timestamps are far below the int64 overflow boundary
}

// ethBlock decodes [fixtureBlock.RLP]; the C-Chain's libevm extras MUST be
// registered, as documented on that field.
func (b fixtureBlock) ethBlock(tb testing.TB) *types.Block {
	tb.Helper()

	eth := new(types.Block)
	require.NoError(tb, rlp.DecodeBytes(b.RLP, eth), "rlp.DecodeBytes(block %d)", b.Number)
	return eth
}

// hasNativeAssetCall reports whether b is one of the
// [nativeAssetCallBlocks].
func (b fixtureBlock) hasNativeAssetCall() bool {
	return slices.Contains(nativeAssetCallBlocks, b.Number)
}

// accountState is the post-execution balance and nonce of an account.
type accountState struct {
	Balance *hexutil.Big `json:"balance"`
	Nonce   uint64       `json:"nonce"`
	// ANTBalance is the account's multicoin balance of [fixture.ANTAssetID].
	ANTBalance *hexutil.Big `json:"antBalance"`
}

// Heights ([fixtureBlock.Number]) of fixture blocks that consuming tests
// single out. The fixture is frozen, so they cannot drift.
var (
	// nativeAssetCallBlocks carry a functional nativeAssetCall, whose nested
	// EVM call makes tracing fail with [nativeAssetCallTraceError] — in the
	// historical C-Chain and in any replaying VM.
	nativeAssetCallBlocks = []uint64{7, 11}

	// deprecatedNativeAssetCallBlock's nativeAssetCall ran while the
	// precompile was deprecated, giving the fixture's only failed receipt.
	deprecatedNativeAssetCallBlock uint64 = 10

	// sendWarpMessageBlock's sendWarpMessage logs the warp precompile's
	// SendWarpMessage event.
	sendWarpMessageBlock uint64 = 15
)

const (
	// nativeAssetCallTraceError is the exact error from the debug_trace*
	// endpoints on [nativeAssetCallBlocks].
	nativeAssetCallTraceError = "incorrect number of top-level calls"

	// genesisTraceError is the exact error from the debug_trace* and
	// debug_intermediateRoots endpoints on the genesis block.
	genesisTraceError = "genesis is not traceable"
)
